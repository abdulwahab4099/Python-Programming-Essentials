""" Define points (x0, y0) and (x1, y1) """

x0 = 2

y0 = 3

x1 = 5

y1 = 7

""" Calculate the distance between the points using the Euclidean distance formula """
# distance = sqrt((x1 - x0)^2 + (y1 - y0)^2)
distance = ((x1 - x0)**2 + (y1 - y0)**2)**0.5
print(distance)  
