'''
The circumference of a circle is 2πr where r is the radius of the circle. 
Write a Python statement that calculates and prints the circumference in inches of a circle 
whose radius is 8 inches. Assume that the constant π=3.14.
'''
π=3.14
inch=1
radius=8*inch

circumference=2*π*radius

print('The circumference of the circle is', circumference, 'inches.')