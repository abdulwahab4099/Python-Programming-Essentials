'''
The perimeter of a rectangle is 2w+2h, where w and h are the lengths of its sides. 
Write a Python statement that calculates and prints the length in inches of the perimeter of a rectangle 
with sides of length 4 and 7 inches.
'''
inch=1
length=4*inch

width=7*inch

perimeter=2*length+2*width

print(perimeter)

