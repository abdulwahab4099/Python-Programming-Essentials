'''
The area of a circle is π(r)^2, where r is the radius of the circle. 
(The raised 2 in the formula is an exponent.) 
Write a Python statement that calculates and prints the area in square inches of a circle 
whose radius is 8 inches. Assume that the constant π=3.14
'''
π=3.14
inch=1
radius=8*inch

area=π*radius**2

print('The area of the circle is', area, 'square inches.')